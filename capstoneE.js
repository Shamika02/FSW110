function add(){
    fst=parseInt(myShamika.fst.value);
    snd=parseInt(myShamika.snd.value);
    trd=parseInt(myShamika.trd.value);
    result=fst+snd+trd;
    myShamika.result.value=result;
   
}

function subtract(){
    fst=parseInt(myShamika.fst.value);
    snd=parseInt(myShamika.snd.value);
    trd=parseInt(myShamika.trd.value);
    result=fst-snd-trd;
    myShamika.result.value=result;
   
}

function multiply(){
    fst=parseInt(myShamika.fst.value);
    snd=parseInt(myShamika.snd.value);
    trd=parseInt(myShamika.trd.value);
    result=fst*snd*trd;
    myShamika.result.value=result;
}